package org.archiviststoolkit.report;

public abstract class ATReport implements Comparable {

	public static final String REPORT_TYPE_JASPER = "jasper";
	public static final String REPORT_TYPE_FINDING_AID = "findingAid";


	private String reportTitle;
	private String reportDescription;
	private String reportType;

	public int compareTo(Object o) {
		return this.toString().compareTo(o.toString());
	}

	public String toString() {
		return this.getReportTitle();
	}

	public String getReportType() {
		return reportType;
	}

	public void setReportType(String reportType) {
		this.reportType = reportType;
	}

	public String getReportTitle() {
		return reportTitle;
	}

	public void setReportTitle(String reportTitle) {
		this.reportTitle = reportTitle;
	}

	public String getReportDescription() {
		return reportDescription;
	}

	public void setReportDescription(String reportDescription) {
		this.reportDescription = reportDescription;
	}
}
